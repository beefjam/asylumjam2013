# Hi there!

Welcome to the ZeroBrane Studio tour. ZeroBrane Studio is an application that provides you with everything you need to create and run your own programs. 

In about 15 minutes you will know enough to create, save, and run programs in this environment. It will take longer to figure out **what** you want your programs to do and **how** exactly you should write them to do that, but fear not! Our team is working on more tutorials to help you learn programming, graphics, and other topics you may find interesting.

Click the [next](02-controls.lua) link at the top of the screen to get started.
